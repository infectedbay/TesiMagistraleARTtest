    model.Add(Ro_ADMINISTRATIVE != Ro_DIAGNOSIS)
    model.Add(Ro_ADMINISTRATIVE != Rc1_DIAGNOSIS)
    model.Add(Ro_ADMINISTRATIVE != Ro_MEDICAL)
