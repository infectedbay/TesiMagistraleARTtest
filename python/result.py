    C_Rc1_ADMINISTRATIVE = model.NewIntVarFromDomain(cp_model. Domain.FromIntervals([[40],[30],[55],[95],[90],[85],[10],[25],[70],[100]]), 'C_Rc1_ADMINISTRATIVE')
